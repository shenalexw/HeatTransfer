H=0.25;
r1=0.025;
Tw=500;
Tb=25;
k=186;
t=0.002;
N=5;
r2=0.05;
h=[1:1:200];
L=r2-r1;
Lc=L+0.5*t;
Am=Lc.*t;
r2c=r1+Lc;
Af=2*pi*(r2c.^2-r1^2);
Lb=H-N*t;
Ab=pi*Lb*2*r1;
mL=sqrt(2*h./(k.*Am)).*Lc.^1.5;
nf=tanh(mL)./mL;
qf=N.*nf.*h.*Af*(Tw-Tb);
qb=h*Ab*(Tw-Tb);
qtot=qf+qb;
plot(h,qtot);
xlabel('Film Heat Transfer Coefficient (W/m^2)');
ylabel('heat loss (W)');
title('Effect Film Heat Transfer Coefficient Has On Heat Loss');